class Solution{
    //Function to find the leaders in the array.
    static ArrayList<Integer> leaders(int arr[], int n){
        // Your code here
        ArrayList<Integer> arr1 = new ArrayList<Integer>();
        arr1.add(arr[n - 1]);
        if(n == 1){
            return arr1;
        }
        int check = arr[n - 1];
        for(int i = n - 2;i>=0;i--){
            if(arr[i] >= check){
                arr1.add(0, arr[i]);
                check = arr[i];
            }
        }
        
        return arr1;
    }
}
