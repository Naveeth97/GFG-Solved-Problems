#Function to find the data of kth node from the end of a linked list
class Solution:
    def getKthFromLast(self, head, k):
        #code here
        
        temp = head
        k -= 1
        
        
        while k >=1:
            
            temp = temp.next
            
            if temp == None:
                return -1
            k -= 1
                
                
        while temp.next != None:
            
            temp = temp.next
            head = head.next
            
        return head.data


